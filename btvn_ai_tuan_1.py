from geopy.distance import geodesic
from IPython.display import display

kho = [
    {"ten": "Kho 1", "toa_do": (10.7769, 106.6953)},
    {"ten": "Kho 2", "toa_do": (10.7820, 106.7000)}
]

don_hang = [
    (10.7800,106.6980),
    (10.7790,106.6940),
    (10.7750,106.6900),
    (10.7720,106.6920),
    (10.7710,106.6970),
    (10.7830,106.7030)
]

def tim_kho_gan_nhat(diem):
    return min(kho, key=lambda k: geodesic(diem, k["toa_do"]).km)

def tao_tuyen_toi_uu(start, points):
    chua_di = points.copy()
    hien_tai = start
    tuyen = [start]
    tong = 0

    while chua_di:
        diem_gan = min(chua_di, key=lambda p: geodesic(hien_tai, p).km)
        tong += geodesic(hien_tai, diem_gan).km
        hien_tai = diem_gan
        tuyen.append(diem_gan)
        chua_di.remove(diem_gan)

    return tuyen, tong

def tao_tuyen_ngau_nhien(start, points):
    ds = points.copy()
    random.shuffle(ds)
    tuyen = [start] + ds
    tong = 0

    for i in range(len(tuyen)-1):
        tong += geodesic(tuyen[i], tuyen[i+1]).km

    return tuyen, tong

ban_do = folium.Map(location=[10.7769,106.6953], zoom_start=14)

tong_opt = 0
tong_random = 0

for k in kho:
    diem_kho = k["toa_do"]
    ds_khach = [d for d in don_hang if tim_kho_gan_nhat(d) == k]

    if not ds_khach:
        continue

    tuyen_opt, kc_opt = tao_tuyen_toi_uu(diem_kho, ds_khach)
    tuyen_rand, kc_rand = tao_tuyen_ngau_nhien(diem_kho, ds_khach)

    tong_opt += kc_opt
    tong_random += kc_rand

    folium.PolyLine(tuyen_opt, color="green", weight=5, tooltip="Tối ưu").add_to(ban_do)
