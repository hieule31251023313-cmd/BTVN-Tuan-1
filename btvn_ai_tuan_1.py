import osmnx as ox
import networkx as nx
import folium
from IPython.display import display

ox.settings.use_cache = True
ox.settings.log_console = False

place = "District 1, Ho Chi Minh City, Vietnam"

G = ox.graph_from_place(place, network_type="drive")

diem_A = (10.7769, 106.6953)
diem_B = (10.7700, 106.7000)

node_A = ox.distance.nearest_nodes(G, diem_A[1], diem_A[0])
node_B = ox.distance.nearest_nodes(G, diem_B[1], diem_B[0])

route_dijkstra = nx.shortest_path(G, node_A, node_B, weight="length")

def heuristic(u, v):
    y1, x1 = G.nodes[u]["y"], G.nodes[u]["x"]
    y2, x2 = G.nodes[v]["y"], G.nodes[v]["x"]
    return ((y1 - y2)**2 + (x1 - x2)**2)**0.5

route_astar = nx.astar_path(G, node_A, node_B, heuristic=heuristic, weight="length")

def get_length(graph, route):
    tong = 0
    for u, v in zip(route[:-1], route[1:]):
        data = graph.get_edge_data(u, v)
        if data is not None:
            canh_ngan_nhat = min(data.values(), key=lambda x: x["length"])
            tong += canh_ngan_nhat["length"]
    return tong

len_dijkstra = get_length(G, route_dijkstra)
len_astar = get_length(G, route_astar)

print("Chiều dài Dijkstra (m):", round(len_dijkstra, 2))
print("Chiều dài A* (m):", round(len_astar, 2))

m = folium.Map(location=diem_A, zoom_start=14)

coords_dijkstra = [(G.nodes[n]["y"], G.nodes[n]["x"]) for n in route_dijkstra]
coords_astar = [(G.nodes[n]["y"], G.nodes[n]["x"]) for n in route_astar]

folium.PolyLine(
    coords_dijkstra,
    color="blue",
    weight=5,
    tooltip="Dijkstra"
).add_to(m)

folium.PolyLine(
    coords_astar,
    color="red",
    weight=3,
    tooltip="A*"
).add_to(m)

folium.Marker(
    diem_A,
    popup="Điểm A",
    icon=folium.Icon(color="green")
).add_to(m)

folium.Marker(
    diem_B,
    popup="Điểm B",
    icon=folium.Icon(color="red")
).add_to(m)

display(m)

print()
print("NHẬN XÉT")
print("- Dijkstra tìm đường đi ngắn nhất chính xác nhưng thường duyệt nhiều nút hơn.")
print("- A* dùng heuristic nên thường tìm nhanh hơn.")
print("- Nếu heuristic phù hợp thì kết quả A* và Dijkstra thường rất gần nhau hoặc giống nhau.")
print("- Trong giao thông thông minh, A* phù hợp cho tìm đường thời gian thực.")

